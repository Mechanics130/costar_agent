import { spawnSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const repoRoot = process.cwd();
const args = process.argv.slice(2);
const checkOnly = args.includes("--check");
const outputArg = valueAfter("--output");
const outputPath = path.resolve(repoRoot, outputArg || "docs/generated-file-map.md");
const outputRel = toRepoPath(outputPath);

const files = listRepoFiles()
  .filter((file) => file !== outputRel)
  .sort((left, right) => left.localeCompare(right));
const byTopLevel = groupByTopLevel(files);
const markdown = renderMarkdown({ files, byTopLevel, outputRel });

if (checkOnly) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
  if (current !== markdown) {
    console.error(`${outputRel} is stale. Run npm run docs:file-map.`);
    process.exit(1);
  }
  console.log(`${outputRel} is up to date.`);
  process.exit(0);
}

writeFileSync(outputPath, markdown, "utf8");
console.log(`Wrote ${outputRel}`);

function valueAfter(flag) {
  const index = args.indexOf(flag);
  if (index === -1) {
    return "";
  }
  return args[index + 1] || "";
}

function listRepoFiles() {
  const result = runGit(["ls-files", "--cached", "--others", "--exclude-standard"]);
  return result
    .split(/\r?\n/)
    .map((item) => item.trim())
    .filter(Boolean)
    .filter((item) => !item.startsWith("node_modules/"));
}

function groupByTopLevel(paths) {
  const groups = new Map();
  for (const file of paths) {
    const top = file.includes("/") ? file.split("/")[0] : "(root)";
    if (!groups.has(top)) {
      groups.set(top, []);
    }
    groups.get(top).push(file);
  }
  return [...groups.entries()].sort(([left], [right]) => left.localeCompare(right));
}

function renderMarkdown({ files, byTopLevel, outputRel }) {
  const lines = [
    "# CoStar Generated File Map",
    "",
    "This file is generated by `npm run docs:file-map`.",
    `The generated file itself is excluded from the file count: \`${outputRel}\`.`,
    "Branch and commit metadata are intentionally omitted so the file stays stable after commits and merges.",
    "",
    "## Repository Snapshot",
    "",
    `- Tracked / public candidate files counted: ${files.length}`,
    "",
    "## Top-Level Map",
    "",
    "| Area | File count | Purpose |",
    "| --- | ---: | --- |",
  ];

  for (const [area, areaFiles] of byTopLevel) {
    lines.push(`| \`${area}\` | ${areaFiles.length} | ${purposeFor(area)} |`);
  }

  lines.push(
    "",
    "## Important Entrypoints",
    "",
    "- `bin/costar.mjs`: CLI for engine mode and host-model installs.",
    "- `costar-core/`: shared stores, commits, host tool contracts, and MCP bridge.",
    "- `integrations/claude/`: Claude host-model bundle.",
    "- `integrations/codex/`: Codex host-model skill bundle.",
    "- `integrations/openclaw/`: OpenClaw host-model bundle.",
    "- `relationship-briefing/`: Briefing runtime and enhanced insight prompt.",
    "- `scripts/check-public-repo.mjs`: public hygiene scanner.",
    "",
    "## Full File List",
    ""
  );

  for (const [area, areaFiles] of byTopLevel) {
    lines.push(`### ${area}`, "");
    areaFiles.forEach((file) => lines.push(`- \`${file}\``));
    lines.push("");
  }

  return `${lines.join("\n").trim()}\n`;
}

function purposeFor(area) {
  const purposes = {
    ".github": "GitHub community, issue, PR, and CI metadata",
    "(root)": "Repository metadata and entry docs",
    assets: "Branding and public assets",
    bin: "CLI entrypoint",
    "costar-core": "Shared core, stores, host-model tools, commits, MCP bridge",
    docs: "Public architecture, support, testing, and product notes",
    examples: "Public mock examples",
    integrations: "Host adapters for Claude, Codex, and OpenClaw",
    "relationship-briefing": "Brief generation",
    "relationship-capture": "User-facing import orchestration",
    "relationship-graph": "Graph and review resolution",
    "relationship-ingestion": "Extraction and profile review",
    "relationship-profile": "Durable profile read / write",
    "relationship-roleplay": "Conversation simulation",
    "relationship-view": "Persistent markdown views",
    scripts: "Checks, smoke tests, and generated docs"
  };
  return purposes[area] || "Project files";
}

function runGit(gitArgs) {
  const result = spawnSync("git", gitArgs, {
    cwd: repoRoot,
    encoding: "utf8"
  });
  if (result.status !== 0) {
    throw new Error(result.stderr || result.stdout || `git ${gitArgs.join(" ")} failed`);
  }
  return result.stdout.trim();
}

function toRepoPath(absPath) {
  return path.relative(repoRoot, absPath).replace(/\\/g, "/");
}
